#include "3-calc.h"

/**
 * op_add - performs addition
 * @a: first int
 * @b: second int
 * Return: 0 on success
 */

int op_add(int a, int b)
{
	return a + b;
}

/**
 * op_sub - performs subraction
 * @a: first int
 * @b: second int
 * Return: 0 on success
 */
int op_sub(int a, int b)
{
	return a - b;
}


/**
 * op_mul - Performs multiplication
 * @a: first int
 * @b: second int
 * Return: 0 on success
 */
int op_mul(int a, int b)
{
	return a * b;
}
/**
 * op_div - Performs Division
 * @a: first int
 * @b: second int
 * Return: 0 on success
 */
int op_div(int a, int b)
{
	return a / b;
}
/**
 * op_mod - returns remainder after a division
 * @a: first int
 * @b: second int
 * Return: 0 on suceess
 */
int op_mod(int a, int b)
{
	return a % b;
}



