#include "3-calc.h"

/**
 * op_add - sums of two int
 * @a: int one
 * @b: int two
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: int 1
 * @b: int 2
 * Return: Difference between int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divides two int
 * @a: first int
 * @b: second int
 * Return: result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - multiply integers
 * @a: int 1
 * @b: int 2
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod -calculates  remainder after division of int
 * @a: int 1
 * @b: int 2
 * Return: returns remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
